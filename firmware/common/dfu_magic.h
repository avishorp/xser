// dfu_magic.h
//
// Magic number used to designate the bootloader to enter DFU (programming)
// mode.

#ifndef __DFU_MAGIC__
#define __DFU_MAGIC__

#define DFU_MAGIC_1     0x45
#define DFU_MAGIC_2     0xe3
#define DFU_MAGIC_3     0x73
#define DFU_MAGIC_4     0xf0

#endif // __DFU_MAGIC__